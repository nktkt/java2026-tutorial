// ============================================================================
// パッケージ宣言: このクラスが payment_project.model パッケージに属することを示す
// Product.java と同じ model パッケージに配置することで、
// データモデル同士の関連性を明確にしている（パッケージによる論理的グルーピング）
// ============================================================================
package payment_project.model;

/**
 * カート内の1商品（商品 + 数量）を表すモデルクラス
 *
 * 【デザインパターン・設計思想】
 * - 集約パターン（Composition）: CartItem は Product を「持っている」（has-a 関係）
 *   Product を継承（is-a）するのではなく、フィールドとして保持する設計
 *   → これにより、同じ Product を異なる数量で複数のカートに入れることが可能
 * - Value Object 的な側面: CartItem は「ある商品が何個カートに入っているか」という
 *   一時的な状態を表すオブジェクトであり、DBに永続化する必要はない
 *   （セッション上に一時保存され、注文確定後に Order に変換される）
 *
 * 【Webアプリケーションでのデータフロー】
 * 1. ユーザーが「カートに追加」ボタンを押す
 * 2. CartServlet が CartService.addItem() を呼び、CartItem が生成される
 * 3. CartItem は HttpSession 内の CartService に保持される
 * 4. カート画面（cart.jsp）で CartItem のリストが表示される
 * 5. 決済時に CartItem のリストが Order オブジェクトに変換される
 *
 * 【セキュリティに関する注意点】
 * - CartItem の price 情報は Product オブジェクト経由で取得する
 *   → クライアントから送信された価格を使わず、サーバー側の Product の価格を使うことで
 *     価格改ざん攻撃を防止できる
 * - セッション（HttpSession）に格納されるため、Serializable インターフェースの
 *   実装を検討すべき（クラスタ環境でのセッションレプリケーション対応）
 */
public class CartItem {

    // ========================================================================
    // フィールド定義
    // ========================================================================

    // このカートアイテムが参照する商品オブジェクト
    // Product オブジェクトへの参照を保持することで、商品名・価格・在庫情報にアクセスできる
    // 【重要】 ここでは Product のコピーではなく「参照」を保持している
    // つまり、Product の在庫数が変更されると、CartItem 経由でも変更後の値が見える
    // これは PaymentService で在庫を減らす際に、整合性を保つために意図的にこう設計されている
    private Product product;

    // カートに入れた数量
    // 1以上の正の整数であるべき（0以下の場合は CartService 側でバリデーションする）
    // 在庫数を超える数量は設定できない（CartService.addItem() でチェック）
    private int quantity;

    // ========================================================================
    // コンストラクタ
    // ========================================================================

    // CartItem を生成するコンストラクタ
    // 引数として商品オブジェクトと数量を受け取る
    // デフォルトコンストラクタは提供しない（product と quantity は必須のため）
    // → 不完全な状態の CartItem が生成されるのを防ぐ設計
    // CartService.addItem() メソッド内で new CartItem(product, quantity) として呼ばれる
    public CartItem(Product product, int quantity) {
        this.product = product;   // 引数の商品オブジェクトをフィールドに代入する
        this.quantity = quantity;  // 引数の数量をフィールドに代入する
    }

    // ========================================================================
    // getter/setter メソッド
    // ========================================================================

    // 商品オブジェクトのgetter: このカートアイテムに関連付けられた Product を返す
    // JSP では ${cartItem.product.name} のようにドットチェーンで商品名にアクセスできる
    // getProduct() → Product オブジェクト → getName() の順で呼ばれる
    public Product getProduct() { return product; }

    // 数量のgetter: カートに入れた数量を返す
    // カート画面で「数量: 3」のように表示する際や、小計計算に使用する
    public int getQuantity() { return quantity; }

    // 数量のsetter: カートに入れた数量を変更する
    // CartService.updateQuantity() や CartService.addItem()（数量加算時）から呼ばれる
    // 【注意】 product の setter は提供していない（カートアイテムの商品を途中で変更する必要はないため）
    // これは意図的な設計で、不要な変更を防ぐ「最小権限の原則」に従っている
    public void setQuantity(int quantity) {
        this.quantity = quantity; // 新しい数量をフィールドに代入する
    }

    // ========================================================================
    // ビジネスロジックメソッド
    // ========================================================================

    // この商品の小計（税込）を計算して返すメソッド
    // 【計算式】 税込単価 × 数量 = 小計
    // 例: 税抜 2800円の商品 × 2個 → (int)(2800 * 1.10) * 2 = 3080 * 2 = 6160円
    // 【設計上のポイント】
    // - 小計の計算ロジックを CartItem に持たせることで、呼び出し元が計算を意識しなくて済む
    //   （「情報エキスパート」パターン: データを持っているクラスが計算を担当する）
    // - Product の getTaxIncludedPrice() を利用しているので、税率の変更は Product 側だけで済む
    //   → 税率変更時の修正箇所が1か所に集約される（DRY原則: Don't Repeat Yourself）
    // 【Webアプリでの使い方】
    // - JSP の EL式: ${cartItem.subtotal} で小計金額を取得できる
    // - カート画面で各商品の小計を表示したり、合計金額を計算する基礎になる
    public int getSubtotal() {
        return product.getTaxIncludedPrice() * quantity; // 税込単価 × 数量で小計を返す
    }

    // ========================================================================
    // Object クラスのメソッドをオーバーライド
    // ========================================================================

    // toString(): カートアイテムの文字列表現を返す
    // コンソール出力時に「  Java入門書 x2 = 6160円」のように表示される
    // 先頭のスペース2つはインデント（カート内容の一覧表示時に見やすくするため）
    // String.format() のフォーマット指定子:
    //   %s → 文字列（商品名）, %d → 整数（数量、小計金額）
    // getSubtotal() を呼び出しているので、税込の小計金額が表示される
    @Override
    public String toString() {
        return String.format("  %s x%d = %d円",
            product.getName(), quantity, getSubtotal()); // 商品名、数量、小計を整形して返す
    }
}
