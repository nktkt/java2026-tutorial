package lesson16_encapsulation;

/**
 * ===== レッスン16: カプセル化 (Encapsulation) =====
 *
 * 【目標】データを安全に管理する方法を学ぶ
 *
 * 【カプセル化とは？】
 *   フィールドを private にして直接アクセスさせず、
 *   getter/setter メソッド経由でアクセスさせること。
 *   薬のカプセルのように、中身（データ）を外殻（メソッド）で包み込むイメージ。
 *
 * 【なぜ必要？】
 *   - 不正な値の設定を防げる（setterで検証できる）
 *   - 内部実装を変えても外部に影響しない
 *   - 読み取り専用（getterだけ）にできる
 *
 * 【アクセス修飾子】
 *   public     → どこからでもアクセスできる
 *   protected  → 同じパッケージ + 子クラスからアクセスできる
 *   (なし)     → 同じパッケージからのみ（パッケージプライベート）
 *   private    → 同じクラスの中からのみ
 *
 * 【getter/setter の命名規則】
 *   フィールド name の場合:
 *     getter: getName()
 *     setter: setName(String name)
 *   boolean の場合:
 *     getter: isActive() （is + 形容詞）
 */
public class Encapsulation {

    public static void main(String[] args) {

        // ========== 悪い例: publicフィールド ==========
        // フィールドが public だと、外部から自由にアクセスでき、
        // どんな値でも設定できてしまう。バリデーション（値の検証）が一切できない。

        // BadStudent bs = new BadStudent();
        // ↑ BadStudent クラスのインスタンス（オブジェクト）を作成する。
        //   new BadStudent() はデフォルトコンストラクタを呼ぶ。
        //   この時点では name は null、age は 0（int のデフォルト値）。

        // bs.name = "田中";
        // ↑ public フィールドなので、外部から直接代入できてしまう。
        //   フィールドへの直接アクセスは「ドット記法」（オブジェクト.フィールド名）を使う。
        //   これ自体は問題ないが、値の検証ができないのが問題。

        // bs.age = -5;       // ← 不正な値が入れられてしまう！
        // ↑ 年齢に -5 を設定している。現実には年齢がマイナスになることはない。
        //   public フィールドでは、このような不正な値を設定されても防ぐ手段がない。
        //   これがカプセル化をしない場合の最大の問題点。

        // System.out.println("--- 悪い例 ---");
        // ↑ 悪い例のセクションタイトルを表示する。

        // System.out.println(bs.name + ", " + bs.age + "歳");
        // ↑ 出力: "田中, -5歳" となる。-5歳は明らかにおかしいが、
        //   publicフィールドでは防げない。これがバグの温床になる。

        // ========== 良い例: privateフィールド + getter/setter ==========
        // フィールドを private にし、getter/setter メソッド経由でのみアクセスさせる。
        // setter 内にバリデーション（値の検証）を入れることで、不正な値を拒否できる。

        // GoodStudent gs = new GoodStudent("鈴木", 20);
        // ↑ GoodStudent クラスのインスタンスを作成。引数付きコンストラクタを呼ぶ。
        //   "鈴木" は名前、20 は年齢。コンストラクタ内で setAge() を通して検証される。
        //   private フィールドには直接アクセスできないので、gs.name = "..." はコンパイルエラーになる。

        // System.out.println("\n--- 良い例 ---");
        // ↑ 良い例のセクションタイトルを表示する。\n で上に空行を入れて見やすくする。

        // System.out.println(gs.getName() + ", " + gs.getAge() + "歳");
        // ↑ gs.getName() で名前を取得、gs.getAge() で年齢を取得する。
        //   private フィールドに直接アクセスできないので、getter メソッドを使う。
        //   出力: "鈴木, 20歳"
        //   gs.name とは書けない（private なのでコンパイルエラー）。

        //
        // gs.setAge(21);     // OK
        // ↑ setter メソッドで年齢を 21 に更新する。
        //   21 は 0 以上なので、setter 内のバリデーションを通過し、値が更新される。
        //   gs.age = 21; とは書けない（private なのでコンパイルエラー）。

        // System.out.println("更新後: " + gs.getAge());
        // ↑ 更新後の年齢を表示する。出力: "更新後: 21"

        //
        // gs.setAge(-5);     // setterが不正値を拒否する
        // ↑ 年齢に -5 を設定しようとするが、setter 内の if (age < 0) に引っかかる。
        //   setter がエラーメッセージを表示し、return で処理を中断する。
        //   this.age は変更されないので、年齢は 21 のまま。
        //   これがカプセル化の力 -- 不正な値の設定を setter で防御できる。

        // System.out.println("不正値後: " + gs.getAge());  // 変わっていない
        // ↑ setAge(-5) は拒否されたので、年齢は 21 のまま。
        //   出力: "不正値後: 21"
        //   BadStudent なら -5 がそのまま入ってしまうが、GoodStudent は防いでくれる。

        // ===== 練習問題 =====
        // 1. BankAccountクラスを作ろう
        //    - private フィールド: owner(String), balance(int)
        //    - deposit(int amount): 入金（0以上のみ受付）
        //    - withdraw(int amount): 出金（残高以上の出金は拒否）
        //    - getBalance(): 残高照会
        //    【ヒント】deposit と withdraw は setter の役割を持つメソッド。
        //    　不正な操作を防ぐために、メソッド内でバリデーションを行うこと。
    }
}

// === 悪い例: フィールドがpublic ===
// どこからでも自由にフィールドの値を変更できてしまうクラス。
// 値の検証ができないため、不正なデータが入る危険がある。

// class BadStudent {
// ↑ クラス定義。同じファイル内に複数クラスを書くことができる。
//   ただし public クラスはファイルに1つだけ。BadStudent は public を付けていない。
//   これは「パッケージプライベート」で、同じパッケージ内からのみアクセスできる。

//     public String name;
//     ↑ public フィールド。外部からクラスのどこからでも bs.name で直接読み書きできる。
//       値の検証なしに自由に変更できてしまうため、不正な値が入る可能性がある。

//     public int age;    // ← 誰でも好きな値を入れられてしまう
//     ↑ public フィールド。bs.age = -100 のように、ありえない値を設定されても防げない。
//       int のデフォルト値は 0。

// }

// === 良い例: フィールドがprivate + getter/setter ===
// フィールドを private にして直接アクセスを禁止し、
// getter/setter メソッドを通じてのみアクセスさせるクラス。
// setter 内でバリデーションを行うことで、データの整合性を保つ。

// class GoodStudent {
// ↑ カプセル化を正しく適用したクラス。パッケージプライベート。

//     private String name;    // ← 外から直接アクセスできない
//     ↑ private フィールド。このクラスの中からしかアクセスできない。
//       gs.name とは書けない。getName() メソッドを通じてのみ取得できる。
//       private にすることで、外部からの不正なアクセスや変更を防ぐ。

//     private int age;
//     ↑ private フィールド。外部から直接変更できない。
//       setAge() メソッド内でバリデーションを行うことで、不正な値を拒否できる。

//
//     // コンストラクタ
//     public GoodStudent(String name, int age) {
//     ↑ 引数付きコンストラクタ。オブジェクト生成時に名前と年齢を受け取る。
//       public にすることで、他のクラスからインスタンスを作成できるようにする。
//       コンストラクタ名はクラス名と同じ。戻り値の型は書かない。

//         this.name = name;
//         ↑ this.name はこのオブジェクトのフィールド、引数の name は仮引数。
//           同じ名前の場合、this を付けてフィールドと区別する。
//           name の setter を作っていないので、ここで直接代入する。

//         setAge(age);        // setterを通すことで検証もできる
//         ↑ コンストラクタ内でも setter を使って値を設定する。
//           こうすることで、コンストラクタに不正な age が渡されても検証される。
//           this.age = age; と直接代入してしまうと検証を通らないので注意。
//           【ベストプラクティス】コンストラクタ内でも setter を活用する。

//     }
//
//     // getter: 値を返すだけ
//     public String getName() {
//     ↑ getter メソッド。private フィールド name の値を外部から取得するためのメソッド。
//       命名規則: get + フィールド名の先頭を大文字（getName）。
//       戻り値は String 型。引数は不要。

//         return name;
//         ↑ フィールド name の値をそのまま返す。
//           getter は基本的に return 文だけのシンプルなメソッド。

//     }
//
//     // getter
//     public int getAge() {
//     ↑ age フィールドの getter。private フィールドの値を外部に公開する唯一の手段。
//       戻り値は int 型。

//         return age;
//         ↑ フィールド age の値をそのまま返す。

//     }
//
//     // setter: 値を設定する + 検証ロジック
//     public void setAge(int age) {
//     ↑ setter メソッド。private フィールド age の値を設定するためのメソッド。
//       命名規則: set + フィールド名の先頭を大文字（setAge）。
//       戻り値は void（何も返さない）。引数に新しい値を受け取る。
//       ここにバリデーション（値の検証）ロジックを入れるのがカプセル化の核心。

//         if (age < 0) {
//         ↑ バリデーション: 年齢が負の数でないかチェックする。
//           不正な値を検出したら、フィールドを更新せずにメソッドを終了する。

//             System.out.println("エラー: 年齢は0以上にしてください");
//             ↑ 不正な値が設定されようとしたことをユーザーに知らせる。
//               実際のアプリケーションでは、例外を throw することも多い。

//             return;         // 不正な値は設定しない
//             ↑ return で setter メソッドを即座に終了する。
//               この後の this.age = age; は実行されないので、値は変わらない。
//               【重要】return がないと、if の後で不正な値が代入されてしまう。

//         }
//         this.age = age;
//         ↑ バリデーションを通過した場合のみ、ここに到達する。
//           this.age（フィールド）に引数の age を代入して値を更新する。
//           this を使ってフィールドと引数を区別している。

//     }
//
//     // name は setter を作らない → 読み取り専用（変更不可）
//     ↑ getter だけを用意し setter を作らないことで、「読み取り専用」のフィールドにできる。
//       コンストラクタで一度設定した name は、以降変更できない。
//       これも カプセル化の利点の一つ。フィールドごとにアクセスレベルを細かく制御できる。
//       【例】学籍番号、ユーザーID など、作成後に変更すべきでないデータに有効。

// }
