package lesson28_innerclass;

/**
 * ===== レッスン28: 内部クラスと匿名クラス =====
 *
 * 【目標】クラスの中にクラスを定義する4つの方法を理解する
 *
 * 【4種類の内部クラス】
 *   1. static ネストクラス: static class Inner {}
 *      → 外部クラスのインスタンス不要。独立した名前空間として使う。
 *      → 実務で最もよく使われる。外部クラスの static メンバにはアクセスできる。
 *
 *   2. 内部クラス(非static): class Inner {}
 *      → 外部クラスのインスタンスに紐づく。外部のフィールドにアクセス可能。
 *      → 外部クラスの状態を密接に扱う必要がある場合に使う（使用頻度は低め）
 *
 *   3. ローカルクラス: メソッド内で定義
 *      → そのメソッド内でのみ使える。ほとんど使わない。
 *
 *   4. 匿名クラス: new インターフェース() { 実装 }
 *      → 1回限りの実装。ラムダ式の前身。
 *      → Java 8以前のコードで頻繁に見かける。現在はラムダ式が推奨。
 *
 * 【匿名クラス vs ラムダ式】
 *   匿名クラス: メソッドが複数あるインターフェースにも使える
 *   ラムダ式: 関数型インターフェース（メソッド1つ）のみ。より簡潔。
 *
 * 【なぜ内部クラスが必要か？】
 *   - 関連性の高いクラスをまとめて整理できる（例: LinkedList の中に Node クラス）
 *   - 外部に公開する必要のないクラスをカプセル化（情報隠蔽）できる
 *   - 1回しか使わない実装をその場で書ける（匿名クラス/ラムダ式）
 *
 * 【よくあるミス】
 *   1. 非staticの内部クラスを外部インスタンスなしで作ろうとする
 *   2. static ネストクラスから外部クラスのインスタンスフィールドにアクセスしようとする
 *   3. 匿名クラス/ラムダ式から外部のローカル変数を変更しようとする（実質finalでないとエラー）
 */
public class InnerClassLesson {

    // 外部クラスのインスタンスフィールド
    // private なのでこのクラスの内部からのみアクセス可能
    // 非staticな内部クラスからは直接アクセスできるが、staticネストクラスからはアクセスできない
    // なぜなら、staticネストクラスは外部クラスのインスタンスに紐づいていないから
    private String outerField = "外部クラスのフィールド";

    // --- 1. staticネストクラス ---
    // static修飾子がついた内部クラス。外部クラスのインスタンスがなくても使える。
    // 【使い方の例】LinkedListの中のNodeクラス、Mapの中のEntryクラスなど
    // 外部クラスの名前空間の中にあるだけで、実質的には独立したクラスと同じ
    // 【実務Tips】迷ったらstaticネストクラスにする。非staticが必要な場面は限られている。

    // static class StaticNested {

    //     StaticNestedクラスのメソッド。staticネストクラスの中では通常のメソッドが定義できる
    //     void show() {

    //         staticネストクラスであることを示すメッセージを出力する
    //         System.out.println("staticネストクラス");

    //         以下の行はコンパイルエラーになる！
    //         outerField はインスタンスフィールド（非static）であり、
    //         staticネストクラスは外部クラスのインスタンスへの暗黙の参照を持たないため
    //         つまり「どのInnerClassLessonオブジェクトのouterFieldか？」が分からない
    //         これがstatic vs 非static内部クラスの最大の違い
    //         // System.out.println(outerField);  // ← エラー！staticなので外部インスタンスにアクセス不可

    //     }
    // }

    // --- 2. 内部クラス(非static) ---
    // static修飾子がない内部クラス。外部クラスのインスタンスに紐づく。
    // 生成時に「外部インスタンス.new Inner()」という特殊な構文が必要
    // 【内部動作】非static内部クラスは、外部クラスインスタンスへの隠れた参照を保持している
    // そのため、外部インスタンスのフィールドやメソッドに自由にアクセスできる
    // 【注意】外部インスタンスへの参照を保持するため、メモリリークの原因になることがある

    // class Inner {

    //     Innerクラスのメソッド。外部クラスのインスタンスフィールドにアクセスできる
    //     void show() {

    //         outerField は外部クラス InnerClassLesson のインスタンスフィールドに直接アクセスしている
    //         これは非staticな内部クラスが外部インスタンスへの暗黙の参照を持つため可能
    //         より正確には InnerClassLesson.this.outerField と書くこともできる
    //         System.out.println("内部クラス: " + outerField);  // 外部フィールドにアクセス可能

    //     }
    // }

    // main メソッド: プログラムのエントリーポイント（実行開始地点）
    // static メソッドなので、InnerClassLesson のインスタンスがなくても呼べる
    public static void main(String[] args) {

        // --- staticネストクラスの使用 ---
        // staticネストクラスは外部クラスのインスタンスなしで直接生成できる
        // 「new StaticNested()」だけでOK（mainがInnerClassLesson内にあるので外部クラス名省略可）
        // クラス外部からは「new InnerClassLesson.StaticNested()」と書く
        // 通常のクラスの new と同じ感覚で使えるため、最も使いやすい内部クラス
        // StaticNested sn = new StaticNested();    // 外部インスタンス不要

        // staticネストクラスのメソッドを呼ぶ。通常のオブジェクトと同じ使い方
        // sn.show();

        // --- 内部クラスの使用 ---
        // 非staticな内部クラスを使うには、まず外部クラスのインスタンスが必要
        // なぜなら、内部クラスは外部インスタンスの状態（フィールド）にアクセスするため
        // 「どの外部オブジェクトに紐づくか」を決める必要がある
        // InnerClassLesson outer = new InnerClassLesson();

        // outer.new Inner() という特殊な構文で内部クラスのインスタンスを生成する
        // この「outer.new」は「outer という外部インスタンスに紐づいた Inner を作る」という意味
        // 【重要】この構文は Java 特有で他の言語にはあまりない。面接でもよく聞かれる
        // Inner inner = outer.new Inner();          // 外部インスタンスが必要

        // 内部クラスのメソッドを呼ぶ。内部では outer の outerField にアクセスできる
        // inner.show();

        // --- 匿名クラス ---
        // 匿名クラス（Anonymous Class）は名前のないクラスで、その場で定義して即座にインスタンス化する
        // 主にインターフェースの1回限りの実装に使われる
        // 「new Runnable() { ... }」は「Runnableインターフェースを実装した名前のないクラスを作り、
        // そのインスタンスを生成する」という意味

        // Runnable r = new Runnable() {

        //     @Override アノテーション: Runnable インターフェースの run() メソッドを
        //     オーバーライド（上書き実装）していることをコンパイラに明示する
        //     これにより、メソッド名のタイポをコンパイル時に検出できる
        //     @Override

        //     Runnableインターフェースで定義されている唯一の抽象メソッド run() を実装する
        //     このメソッドが、r.run() で呼ばれたときに実行される処理
        //     public void run() {

        //         匿名クラス内の実装内容。文字列を標準出力に表示する
        //         System.out.println("匿名クラスで実装");

        //     }
        // };

        // 作成した匿名クラスのインスタンスの run() メソッドを呼び出す
        // 出力: "匿名クラスで実装"
        // 【注意】Thread と一緒に使う場合は new Thread(r).start() で別スレッドで実行できる
        // r.run();

        // ラムダ式で同じこと（こちらが推奨）
        // ラムダ式（Lambda Expression）は Java 8 で導入された構文で、匿名クラスの簡潔な書き方
        // 「() -> 式」は「引数なしで、式を実行するメソッドを持つオブジェクト」を作る
        // Runnable は関数型インターフェース（抽象メソッドが1つだけ）なのでラムダ式が使える
        // 匿名クラスの約5行が、ラムダ式なら1行で書ける → 可読性が大幅に向上
        // 【実務Tips】関数型インターフェースの実装は常にラムダ式を使うのが現代Javaの標準
        // Runnable r2 = () -> System.out.println("ラムダ式で実装");

        // ラムダ式で作成したオブジェクトのメソッドを呼び出す。出力: "ラムダ式で実装"
        // 内部的には匿名クラスとほぼ同じ動作だが、コンパイル結果は少し異なる
        // （ラムダ式はinvokedynamic命令を使い、匿名クラスより効率的な場合がある）
        // r2.run();

        // ===== 練習問題 =====
        // 1. staticネストクラスで Node（値とnext）を定義してみよう
        //    ヒント: static class Node { int value; Node next; }
        //    これは連結リスト(LinkedList)の基本構造。staticネストクラスの最も一般的な使い方
        // 2. Comparator を匿名クラスで実装してからラムダ式に書き換えてみよう
        //    ヒント（匿名クラス版）: Comparator<String> comp = new Comparator<String>() {
        //        @Override public int compare(String a, String b) { return a.length() - b.length(); }
        //    };
        //    ヒント（ラムダ式版）: Comparator<String> comp = (a, b) -> a.length() - b.length();
    }
}
