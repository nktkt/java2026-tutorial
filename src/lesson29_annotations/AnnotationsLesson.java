package lesson29_annotations;

/**
 * ===== レッスン29: アノテーション =====
 *
 * 【目標】Javaのアノテーションの意味と使い方を理解する
 *
 * 【アノテーションとは？】
 *   コードにメタ情報を付与する仕組み。@ マークで始まる。
 *   コンパイラへのヒント、実行時の情報、フレームワークの設定などに使われる。
 *   アノテーション自体はプログラムの動作を変えない「注釈」だが、
 *   コンパイラやフレームワークがこの情報を読み取って処理を行う。
 *
 * 【よく使うアノテーション】
 *   @Override            → 親クラスのメソッドをオーバーライドしていることを明示
 *   @Deprecated          → 非推奨であることを示す（使うと警告が出る）
 *   @SuppressWarnings    → 特定の警告を抑制する
 *   @FunctionalInterface → 関数型インターフェースであることを明示
 *
 * 【フレームワークでの利用例（参考）】
 *   @Test (JUnit)         → テストメソッド
 *   @Autowired (Spring)   → 依存性の自動注入
 *   @Entity (JPA)         → データベースのテーブルに対応
 *
 * 【なぜアノテーションが重要か？】
 *   - コンパイル時にバグを早期発見できる（@Override でタイポ防止）
 *   - コードの意図をドキュメントとして明示できる（@Deprecated で「使わないで」と伝える）
 *   - フレームワークの設定をXMLではなくコード内に書ける（Spring Bootなど）
 *   - 実務では毎日のように使う最重要機能の1つ
 *
 * 【よくあるミス】
 *   1. @Override を書き忘れてオーバーライドしたつもりができていない
 *   2. @SuppressWarnings で警告を安易に抑制して、本来対処すべき問題を隠してしまう
 *   3. @Deprecated なメソッドを新規コードで使ってしまう
 */
public class AnnotationsLesson {

    public static void main(String[] args) {

        // ---------- @Override の使用例 ----------

        // Animal 型の変数に Cat のインスタンスを代入する（ポリモーフィズム）
        // 変数の型は Animal だが、実際のオブジェクトは Cat
        // speak() を呼ぶと、Cat クラスでオーバーライドされた版が実行される（動的ディスパッチ）
        // 【重要】これがオブジェクト指向の「多態性（ポリモーフィズム）」の基本
        // Animal cat = new Cat();

        // cat.speak() で Cat の speak() が呼ばれる
        // Animal 型として宣言しているが、実際の型（Cat）のメソッドが実行される
        // 出力: "ニャー"（Animal の "..." ではない）
        // cat.speak();   // オーバーライドされた版が呼ばれる

        // ---------- @Deprecated の使用例 ----------

        // @Deprecated が付いたメソッドを呼び出す
        // IDE（IntelliJ, Eclipse等）上ではメソッド名に取り消し線（~~oldMethod~~）が表示される
        // コンパイル時に「このメソッドは非推奨です」という警告が出る
        // 動作自体は問題なくするが、将来のバージョンで削除される可能性があることを意味する
        // 【実務Tips】非推奨メソッドを見つけたら、代替メソッドを使うようにコードを修正すべき
        // oldMethod();   // IDE上で取り消し線が表示される

        // ---------- @SuppressWarnings の使用例 ----------

        // @SuppressWarnings("unchecked") はジェネリクス型の未チェック警告を抑制するアノテーション
        // 「unchecked」は型安全でない操作（raw型の使用等）に対する警告を意味する
        // 【どういう場面で使う？】レガシーコードとの互換性のためにraw型を使わざるを得ない場合
        // 【注意】むやみに使うべきではない！警告には理由がある。対処不可能な場合のみ使用
        // @SuppressWarnings("unchecked")  // 未チェック警告を抑制

        // java.util.List（ジェネリクス型パラメータなし）は「raw型」と呼ばれ、
        // Java 5以前の書き方。型安全ではないためコンパイラが警告を出す
        // 正しくは List<Object> rawList = new ArrayList<>() と書くべき
        // ここでは @SuppressWarnings の効果を示すためにあえてraw型を使用している
        // java.util.List rawList = new java.util.ArrayList();
    }

    // ---------- Animalクラス（@Override のデモ用親クラス） ----------

    // static が付いているのは外部クラス(AnnotationsLesson)の中にあるstaticネストクラスだから
    // staticネストクラスなので、AnnotationsLessonのインスタンスがなくても使える
    // 【設計意図】Animal は speak() という共通の振る舞いを持つ基底クラス
    // static class Animal {

    //     speak() は Animal クラスのインスタンスメソッド
    //     子クラスでオーバーライドされることを前提とした基本実装（デフォルトの振る舞い）
    //     "..." は「何も話さない」ことを表現している
    //     void speak() { System.out.println("..."); }

    // }

    // ---------- Catクラス（@Override を使ったオーバーライドの例） ----------

    // Cat は Animal を継承（extends）した子クラス
    // Animal のメソッドを再定義（オーバーライド）して、猫特有の振る舞いを実装する
    // static class Cat extends Animal {

    //     @Override アノテーション: このメソッドが親クラス(Animal)のメソッドをオーバーライドしている
    //     ことをコンパイラに宣言する
    //     【@Overrideがないとどうなる？】コンパイルは通るが、以下のリスクがある:
    //     - メソッド名をタイポしても新しいメソッドとして扱われ、エラーにならない
    //     - 親クラスのメソッドシグネチャが変わっても気づけない
    //     @Override    // ← これがないと typo で新メソッドを作ってしまう危険がある

    //     親クラスの speak() を「ニャー」に上書き（オーバーライド）する
    //     戻り値の型、メソッド名、引数の型と数が親クラスと完全に一致する必要がある
    //     一致しないと @Override がある場合はコンパイルエラーになる（これが安全装置）
    //     void speak() { System.out.println("ニャー"); }

    //     以下は @Override のタイポ検出効果を示す例
    //     "speak" を "speck" とタイポしている
    //     @Override があるので、コンパイラが「speck は親クラスに存在しない」とエラーを出す
    //     もし @Override がなければ、speck() という新しいメソッドが作られてしまい、
    //     バグの原因になる（speak() を呼んでもオーバーライドされず "..." が表示される）
    //     // @Override
    //     // void speck() { }  // ← @Override があると typo をコンパイルエラーにできる

    // }

    // ---------- @Deprecated メソッドの例 ----------

    // @Deprecated アノテーション: このメソッドが非推奨であることを宣言する
    // 【効果】
    //   1. IDE上でこのメソッドの使用箇所に取り消し線が表示される
    //   2. コンパイル時に警告メッセージが出力される
    //   3. Javadoc に @deprecated タグを併記して代替メソッドを案内するのが慣例
    // 【なぜメソッドを消さないのか？】
    //   既存のコードがこのメソッドを使っている可能性があるため、いきなり消すと互換性が壊れる
    //   まず @Deprecated で警告し、十分な移行期間を設けてから削除する
    // 【Java 9以降】@Deprecated(since="バージョン", forRemoval=true) でより詳細に指定可能
    // @Deprecated  // ← このメソッドは非推奨（新しい方を使ってください）

    // oldMethod() は以前使われていたが、現在は非推奨のメソッド
    // 機能自体は動作するが、新しいコードでは使わないことが推奨される
    // static void oldMethod() {

    //     非推奨メソッドの中身。呼べば動作はするが、使うべきではないことを示している
    //     System.out.println("古いメソッド");

    // }

    // ===== 練習問題 =====
    // 1. @Override を外すとどうなるか試してみよう（メソッド名を間違えてみる）
    //    → @Override なしで speak() を speck() とタイポすると、コンパイルは通るが
    //      Animal型で呼んだとき "..." が表示されてしまう（意図しない動作）
    //    → @Override ありだとコンパイルエラーで即座にバグに気づける
    // 2. @Deprecated をつけたメソッドを呼んで、IDEの警告を確認しよう
    //    → IntelliJなら黄色のハイライトと取り消し線で非推奨が視覚的にわかる
}
