package lesson19_recursion;

/**
 * ===== レッスン19: 再帰 (Recursion) =====
 *
 * 【目標】メソッドが自分自身を呼び出す仕組みを理解する
 *
 * 【再帰とは？】
 *   メソッドが自分自身を呼び出すこと。
 *   「大きな問題」を「小さな同じ問題」に分解して解く。
 *
 * 【再帰の2つの要素】
 *   ① ベースケース（終了条件）: これ以上再帰しない条件。必須！
 *   ② 再帰ステップ: 自分自身を呼び出す。問題を小さくする。
 *
 *   ※ ベースケースがないと無限再帰 → StackOverflowError で停止
 *
 * 【コールスタック（呼び出しの仕組み）】
 *   Javaではメソッドを呼び出すたびに「スタックフレーム」がメモリ上に積まれる。
 *   再帰呼び出しも同じで、呼ぶたびに新しいフレームがスタックに push される。
 *   ベースケースに到達すると、順番にフレームが pop されて結果が巻き戻る。
 *
 *   例: factorial(5) のコールスタック:
 *
 *     [呼び出しフェーズ (push)]          [巻き戻しフェーズ (pop)]
 *     factorial(5)  -- push -->           <-- return 120 (= 5 * 24)
 *       factorial(4)  -- push -->         <-- return  24 (= 4 * 6)
 *         factorial(3)  -- push -->       <-- return   6 (= 3 * 2)
 *           factorial(2)  -- push -->     <-- return   2 (= 2 * 1)
 *             factorial(1)  -- push -->   <-- return   1  ★ベースケース
 *
 *   スタックの深さ = 再帰の深さ。深すぎると StackOverflowError になる。
 *   Javaのデフォルトスタックサイズは約512KB〜1MB（環境による）。
 *
 * 【再帰の考え方】
 *   factorial(5) を例に:
 *     factorial(5) = 5 × factorial(4)
 *     factorial(4) = 4 × factorial(3)
 *     factorial(3) = 3 × factorial(2)
 *     factorial(2) = 2 × factorial(1)
 *     factorial(1) = 1  ← ベースケース
 *     → 巻き戻し: 1→2→6→24→120
 *
 * 【再帰 vs ループ】
 *   再帰で解ける問題はループでも解ける（逆も同じ）。
 *   再帰が向いている場面: 木構造の探索、分割統治法、バックトラッキング
 *   ループが向いている場面: 単純な繰り返し（再帰はオーバーヘッドがある）
 *
 * 【よくあるミス・注意点】
 *   ① ベースケースを書き忘れる → 無限再帰 → StackOverflowError
 *   ② 再帰ステップで問題が小さくならない → 無限再帰
 *      例: factorial(n) の中で factorial(n) を呼んでしまう（n-1 にしていない）
 *   ③ 再帰の戻り値を使い忘れる → return を付け忘れて結果が捨てられる
 *   ④ フィボナッチのように同じ計算を何度もする再帰は非常に遅い
 *      → メモ化（計算結果をキャッシュ）や動的計画法で改善できる
 */
