/**
 * ===== エキスパート09: セグメント木 (Segment Tree) =====
 * 出典: Codeforces / AtCoder 上級必須
 *
 * 【問題】
 *   配列に対して以下の操作を高速に行え:
 *   - update(i, val): arr[i] の値を val に変更
 *   - query(l, r):    arr[l]〜arr[r] の合計を求める
 *
 *   ナイーブ: update O(1), query O(n)
 *   累積和: update O(n), query O(1)
 *   セグメント木: 両方 O(log n) ← 更新とクエリの両方が頻繁な場合に最適
 *
 * 【セグメント木とは？】
 *   配列の区間情報を完全二分木で管理するデータ構造。
 *   各ノードが「ある区間の集約値（合計/最小/最大など）」を持つ。
 *
 * 【木の構造 - 配列 {1, 3, 5, 7, 9, 11} の場合】
 *   ノード番号と管理する区間:
 *
 *                    [1] 区間[0,5] = 36 (全体の合計)
 *                   /                    \
 *           [2] 区間[0,2] = 9          [3] 区間[3,5] = 27
 *          /           \               /              \
 *   [4] [0,1]=4    [5] [2,2]=5   [6] [3,4]=16    [7] [5,5]=11
 *    /     \                      /       \
 * [8] [0,0]=1  [9] [1,1]=3   [12] [3,3]=7  [13] [4,4]=9
 *
 *   ノード番号の規則:
 *   - 根: 1
 *   - ノード k の左の子: 2*k、右の子: 2*k+1
 *   - ノード k の親: k/2
 *
 * 【配列サイズが 4*n の理由】
 *   - 完全二分木の最大ノード数は 2*n だが、配列が2のべき乗でない場合、
 *     底辺の右側に空きノードができる
 *   - 安全のため 4*n で確保するのが定番（正確には 2 * 次の2のべき乗）
 *
 * 【query の仕組み - query(1, 3) の例】
 *   求めたい: arr[1]+arr[2]+arr[3] = 3+5+7 = 15
 *
 *   ノード[1] 区間[0,5]: [1,3]と重なる → 子ノードへ
 *     ノード[2] 区間[0,2]: [1,3]と重なる → 子ノードへ
 *       ノード[4] 区間[0,1]: [1,3]と重なる → 子ノードへ
 *         ノード[8] 区間[0,0]: [1,3]の外 → return 0
 *         ノード[9] 区間[1,1]: [1,3]に完全に含まれる → return 3
 *       ノード[5] 区間[2,2]: [1,3]に完全に含まれる → return 5
 *     ノード[3] 区間[3,5]: [1,3]と重なる → 子ノードへ
 *       ノード[6] 区間[3,4]: [1,3]と重なる → 子ノードへ
 *         ノード[12] 区間[3,3]: [1,3]に完全に含まれる → return 7
 *         ノード[13] 区間[4,4]: [1,3]の外 → return 0
 *       ノード[7] 区間[5,5]: [1,3]の外 → return 0
 *
 *   合計: 0 + 3 + 5 + 7 + 0 + 0 = 15
 *
 * 【計算量】
 *   時間計算量:
 *     - build: O(n)     ... 全ノードを1回ずつ処理
 *     - update: O(log n) ... 根から葉まで1本のパスを更新
 *     - query: O(log n)  ... 各レベルで最大4ノードしか訪問しない
 *   空間計算量: O(n)
 *     - 木の配列サイズ 4*n
 *
 * 【競プロTips】
 *   - セグメント木は「区間に対する操作 + 点更新」の万能ツール
 *   - 合計だけでなく、最小値、最大値、GCD、XOR なども管理可能
 *   - 遅延伝播（Lazy Propagation）を追加すると「区間更新 + 区間クエリ」も O(log n)
 *   - AtCoder Library (ACL) にもセグメント木が標準搭載
 *   - BIT（Fenwick Tree）は合計限定だが実装が簡単で定数倍も速い
 *   - 座標圧縮やマージソート木と組み合わせると更に強力
 *
 * 【学べること】セグメント木の構築・更新・クエリ
 */
