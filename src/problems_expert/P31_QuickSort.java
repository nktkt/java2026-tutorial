/**
 * ===== エキスパート01: クイックソート (Quick Sort) =====
 * 出典: LC #912 / 最重要ソートアルゴリズム
 *
 * 【問題】配列をクイックソートで昇順にソートせよ。
 *
 * 【アルゴリズム概要】
 *   クイックソートは「分割統治法（Divide and Conquer）」に基づくソートアルゴリズム。
 *   1. ピボット（基準値）を選ぶ
 *   2. ピボットより小さい要素を左、大きい要素を右に分ける（パーティション）
 *   3. 左右それぞれを再帰的にソート
 *
 * 【具体例で理解する】
 *   配列: [38, 27, 43, 3, 9, 82, 10]
 *
 *   ステップ1: ピボット = 10（ランダムに選択後、末尾に移動したと仮定）
 *   パーティション後: [3, 9, | 10 | 38, 27, 43, 82]
 *       → 10より小さい {3, 9} が左側、大きい {38, 27, 43, 82} が右側
 *
 *   ステップ2: 左部分 [3, 9] を再帰ソート → [3, 9]（既にソート済み）
 *   ステップ3: 右部分 [38, 27, 43, 82] を再帰ソート → [27, 38, 43, 82]
 *
 *   最終結果: [3, 9, 10, 27, 38, 43, 82]
 *
 * 【パーティションの動き（Lomutoスキーム）】
 *   配列: [38, 27, 43, 3, 9, 82, 10]  ピボット=10（末尾）
 *   i = -1（小さい要素の境界）、j がスキャン
 *
 *   j=0: arr[0]=38 > 10  → 何もしない    → i=-1, [38, 27, 43, 3, 9, 82, 10]
 *   j=1: arr[1]=27 > 10  → 何もしない    → i=-1, [38, 27, 43, 3, 9, 82, 10]
 *   j=2: arr[2]=43 > 10  → 何もしない    → i=-1, [38, 27, 43, 3, 9, 82, 10]
 *   j=3: arr[3]=3  <= 10 → i=0, swap(0,3) → i=0, [3, 27, 43, 38, 9, 82, 10]
 *   j=4: arr[4]=9  <= 10 → i=1, swap(1,4) → i=1, [3, 9, 43, 38, 27, 82, 10]
 *   j=5: arr[5]=82 > 10  → 何もしない    → i=1, [3, 9, 43, 38, 27, 82, 10]
 *   最後: swap(i+1=2, high=6)             → [3, 9, 10, 38, 27, 82, 43]
 *   ピボット10はインデックス2に確定!
 *
 * 【計算量】
 *   時間計算量:
 *     - 平均: O(n log n) ... 毎回ほぼ半分に分割される場合
 *     - 最悪: O(n^2)     ... 毎回最小or最大がピボットになる場合（ソート済み配列など）
 *     - ランダムピボットにより、最悪ケースの確率を極めて低くできる
 *   空間計算量:
 *     - O(log n) ... 再帰スタック（平均）
 *     - O(n)     ... 再帰スタック（最悪、片側に偏る場合）
 *
 * 【なぜランダムピボットが重要か】
 *   固定ピボット（例: 常に末尾）だと、ソート済み配列で毎回最大値がピボットになり、
 *   パーティションが片側に偏り O(n^2) になる。
 *   ランダム選択により、期待計算量は常に O(n log n) になる。
 *
 * 【競プロTips】
 *   - Java では Arrays.sort() が int[] に対してデュアルピボットクイックソートを使う
 *   - ハック対策: Arrays.sort(int[]) は最悪 O(n^2) なので、
 *     Integer[] にして Arrays.sort(Integer[], Comparator) を使うとマージソートになり安全
 *   - 実装を問われる場面は少ないが、パーティションのアイデアは
 *     「k番目の要素を求める」Quick Select に応用できる（平均 O(n)）
 *   - in-placeソートなので追加メモリが少なくて済む（マージソートは O(n) 必要）
 *
 * 【学べること】分割統治法、パーティション、ランダム化
 */
