package problems_expert;

/**
 * ===== エキスパート11: 区間DP (Burst Balloons) =====
 * 出典: LC #312
 *
 * 【問題】
 *   N個の風船があり、各風船にコインが書いてある。
 *   風船iを割ると coins[left] * coins[i] * coins[right] のコインを獲得。
 *   （leftとrightはまだ割っていない隣の風船）
 *   獲得コインの合計を最大化せよ。
 *
 * 【例】
 *   nums = [3, 1, 5, 8]
 *
 *   割る順序: 1 → 5 → 3 → 8
 *     風船1を割る: 3*1*5 = 15,  残り: [3, 5, 8]
 *     風船5を割る: 3*5*8 = 120, 残り: [3, 8]
 *     風船3を割る: 1*3*8 = 24,  残り: [8]      ← 境界の仮想風船(coins=1)が隣
 *     風船8を割る: 1*8*1 = 8,   残り: []
 *     合計: 15 + 120 + 24 + 8 = 167
 *
 * 【区間DPとは？】
 *   dp[i][j] = 区間[i,j]の問題の最適値。
 *   区間の長さを短い方から長い方へ徐々に広げて計算する。
 *   「小さい区間の答えから大きい区間の答えを構成する」というアプローチ。
 *
 * 【この問題の発想転換（重要！）】
 *   「どの風船を最初に割るか」で考えると、割った後の隣の風船が変わるため複雑。
 *   → 逆転の発想: 「区間 (i, j) の中で最後に割る風船 k を決める」
 *
 *   最後に割る風船 k を選ぶと:
 *   - k が最後なので、k を割る時の隣は coins[i] と coins[j]（区間外の要素）
 *   - k より左の風船 (i+1, ..., k-1) は既に全て割られている → dp[i][k] で計算済み
 *   - k より右の風船 (k+1, ..., j-1) も既に全て割られている → dp[k][j] で計算済み
 *
 * 【DPの定義】
 *   coins[0] = 1, coins[n+1] = 1 （仮想的な境界風船）
 *   coins[1..n] = 元の風船のコイン
 *
 *   dp[i][j] = 開区間 (i, j) 内の風船を全部割って得られる最大コイン
 *   （i と j 自体は割らない。i, j は境界として残る）
 *
 *   遷移: dp[i][j] = max over k in (i+1,...,j-1) of:
 *     dp[i][k] + coins[i]*coins[k]*coins[j] + dp[k][j]
 *
 *   意味:
 *   - dp[i][k]: k の左側の風船を全部割った結果
 *   - coins[i]*coins[k]*coins[j]: k を最後に割った時のコイン（隣は i と j）
 *   - dp[k][j]: k の右側の風船を全部割った結果
 *
 * 【DPテーブルの構築例: nums = [3, 1, 5, 8]】
 *   coins = [1, 3, 1, 5, 8, 1]  (境界の1を追加)
 *
 *   長さ1の区間:
 *     dp[0][2] = coins[0]*coins[1]*coins[2] = 1*3*1 = 3    (k=1のみ)
 *     dp[1][3] = coins[1]*coins[2]*coins[3] = 3*1*5 = 15   (k=2のみ)
 *     dp[2][4] = coins[2]*coins[3]*coins[4] = 1*5*8 = 40   (k=3のみ)
 *     dp[3][5] = coins[3]*coins[4]*coins[5] = 5*8*1 = 40   (k=4のみ)
 *
 *   長さ2の区間:
 *     dp[0][3]: k=1: dp[0][1]+1*3*5+dp[1][3] = 0+15+15 = 30
 *               k=2: dp[0][2]+1*1*5+dp[2][3] = 3+5+0 = 8
 *               → dp[0][3] = 30
 *     ...（省略）...
 *
 *   最終結果: dp[0][5] = 167
 *
 * 【計算量】
 *   時間計算量: O(n^3)
 *     - 区間の長さ len: O(n) 通り
 *     - 左端 i: O(n) 通り
 *     - 最後に割る風船 k: O(n) 通り
 *     - 合計: O(n) × O(n) × O(n) = O(n^3)
 *   空間計算量: O(n^2)
 *     - dp テーブルのサイズ
 *
 * 【競プロTips】
 *   - 区間DPは「区間を分割する点を全探索」がパターン
 *   - 典型問題: 行列連鎖積、最適二分探索木、回文分割
 *   - n が 500 以下なら O(n^3) は大体間に合う（10^8 程度）
 *   - 「最後に操作する要素を決める」という逆転の発想は区間DPの定番テクニック
 *   - 実装では「区間の長さ」を外側のループにするのがポイント（短い区間から順に計算）
 *
 * 【学べること】区間DP、最適部分構造
 */
