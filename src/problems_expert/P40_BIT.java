/**
 * ===== エキスパート10: Binary Indexed Tree (Fenwick Tree / BIT) =====
 * 出典: Codeforces / AtCoder
 *
 * 【問題】セグメント木と同じ操作を、よりシンプルに実装せよ。
 *   - update(i, delta): arr[i] に delta を加算
 *   - rangeSum(l, r):   arr[l]〜arr[r] の合計を求める
 *
 * 【BITとは？】
 *   ビット演算を使って効率的に累積和を管理するデータ構造。
 *   セグメント木より実装が簡単で、定数倍も速い。
 *   ただし、「合計」のような可逆的な操作にしか使えない
 *   （最小値や最大値は通常のBITでは不可）。
 *
 * 【核心: i & (-i) とは？】
 *   i の最下位ビット（Lowest Set Bit / LSB）を取得する。
 *   例:
 *     i = 6  (0110) → i & (-i) = 2  (0010)
 *     i = 12 (1100) → i & (-i) = 4  (0100)
 *     i = 8  (1000) → i & (-i) = 8  (1000)
 *     i = 7  (0111) → i & (-i) = 1  (0001)
 *
 *   なぜ動くか: -i は2の補数表現で ~i + 1。
 *   i と ~i+1 の AND を取ると、最下位の 1 のビットだけが残る。
 *
 * 【BIT の構造（1-indexed）】
 *   bit[i] は「ある区間の合計」を管理する。
 *   bit[i] が管理する区間の長さ = i & (-i)
 *
 *   例: 配列サイズ 8 の場合
 *   bit[1]  (0001): arr[1]           の合計 (区間長=1)
 *   bit[2]  (0010): arr[1..2]        の合計 (区間長=2)
 *   bit[3]  (0011): arr[3]           の合計 (区間長=1)
 *   bit[4]  (0100): arr[1..4]        の合計 (区間長=4)
 *   bit[5]  (0101): arr[5]           の合計 (区間長=1)
 *   bit[6]  (0110): arr[5..6]        の合計 (区間長=2)
 *   bit[7]  (0111): arr[7]           の合計 (区間長=1)
 *   bit[8]  (1000): arr[1..8]        の合計 (区間長=8)
 *
 * 【prefixSum(i) の動き: i=7 の例】
 *   sum = 0
 *   i=7 (0111): sum += bit[7],  i -= 7&(-7) = i -= 1 → i=6
 *   i=6 (0110): sum += bit[6],  i -= 6&(-6) = i -= 2 → i=4
 *   i=4 (0100): sum += bit[4],  i -= 4&(-4) = i -= 4 → i=0
 *   i=0: ループ終了
 *
 *   結果: bit[7] + bit[6] + bit[4] = arr[7] + arr[5..6] + arr[1..4]
 *        = arr[1] + arr[2] + ... + arr[7] ← 正しい累積和！
 *
 * 【update(i, delta) の動き: i=3 の例】
 *   i=3 (0011): bit[3] += delta,  i += 3&(-3) = i += 1 → i=4
 *   i=4 (0100): bit[4] += delta,  i += 4&(-4) = i += 4 → i=8
 *   i=8 (1000): bit[8] += delta,  i += 8&(-8) = i += 8 → i=16
 *   i=16: i > n なのでループ終了
 *
 *   arr[3] を含む全ての区間ノード（bit[3], bit[4], bit[8]）が更新された！
 *
 * 【計算量】
 *   時間計算量:
 *     - update: O(log n) ... i & (-i) の加算で最大 log n 回ループ
 *     - prefixSum: O(log n) ... i & (-i) の減算で最大 log n 回ループ
 *     - rangeSum: O(log n)  ... prefixSum を2回呼ぶ
 *     - 構築: O(n log n) ... n 回の update（O(n) の構築法もある）
 *   空間計算量: O(n)
 *     - bit 配列のサイズ
 *
 * 【セグメント木との比較】
 *   - BIT: 実装が簡単（数行）、定数倍が速い、メモリ半分
 *   - セグメント木: 汎用性が高い（min/max/GCD 等も可能）、遅延伝播が可能
 *   - 合計の区間クエリ + 点更新 → BIT が第一選択
 *
 * 【競プロTips】
 *   - BIT は「転倒数」の計算に非常によく使われる
 *     （各要素について、それより前にある自分より大きい要素の数を BIT で効率的に数える）
 *   - 2次元BIT: BIT を 2次元に拡張すると、2次元累積和の動的更新が可能
 *   - 0-indexed の配列を 1-indexed に変換する部分（i++ や i+1）に注意
 *   - rangeSum(l, r) = prefixSum(r) - prefixSum(l-1) の「l-1」に注意（l=0 の時）
 *
 * 【学べること】BIT、ビット演算の応用
 */
