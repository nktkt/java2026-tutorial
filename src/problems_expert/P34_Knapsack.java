package problems_expert;

/**
 * ===== エキスパート04: ナップサック問題 (0/1 Knapsack) =====
 * 出典: AtCoder典型 / DP の代表問題
 *
 * 【問題】
 *   N個の品物がある。各品物には重さ(weight)と価値(value)がある。
 *   容量 W のナップサックに入れられる品物の価値の合計の最大値を求めよ。
 *   各品物は1回だけ使える（0/1ナップサック）。
 *
 * 【例】
 *   W=7, items: [(w=2,v=3), (w=3,v=4), (w=4,v=5), (w=5,v=6)]
 *   → 最大価値: 9 (品物1: w=3,v=4 + 品物2: w=4,v=5 = 合計重さ7、合計価値9)
 *
 * 【DPテーブルの定義】
 *   dp[i][j] = 品物0〜i-1 までを考慮して、重さの上限が j の場合の最大価値
 *
 *   各品物について2つの選択肢:
 *   - 入れない: dp[i][j] = dp[i-1][j]       （前の品物までの結果をそのまま引き継ぐ）
 *   - 入れる:   dp[i][j] = dp[i-1][j-w[i-1]] + v[i-1]  （重さ分の余裕が必要）
 *   → 両者の max を取る
 *
 * 【DPテーブルの具体例】
 *   W=7, items: [(w=2,v=3), (w=3,v=4), (w=4,v=5), (w=5,v=6)]
 *
 *   dp[i][j]:  j= 0  1  2  3  4  5  6  7
 *   i=0(なし):    0  0  0  0  0  0  0  0   ← 品物なし→全て0
 *   i=1(w=2,v=3): 0  0  3  3  3  3  3  3   ← j>=2で品物1を入れられる
 *   i=2(w=3,v=4): 0  0  3  4  4  7  7  7   ← j=5で両方入る: 3+4=7
 *   i=3(w=4,v=5): 0  0  3  4  5  7  8  9   ← j=7で品物2+3: 4+5=9
 *   i=4(w=5,v=6): 0  0  3  4  5  7  8  9   ← 品物4は重すぎて改善なし
 *
 *   答え: dp[4][7] = 9
 *
 * 【計算量】
 *   時間計算量: O(n * W)
 *     - 2重ループ: 品物数 n × 容量 W
 *     - 各セルの計算は O(1)
 *   空間計算量: O(n * W)
 *     - DPテーブルのサイズ
 *     - 1次元に最適化すると O(W) にできる（後述）
 *
 * 【1次元DP最適化（発展）】
 *   dp[i][j] は dp[i-1][...] しか参照しないので、1次元配列で十分。
 *   ただし、0/1ナップサックでは j を逆順にループする必要がある。
 *   （順方向だと同じ品物を2回使ってしまう恐れがある）
 *
 *   for (int i = 0; i < n; i++)
 *     for (int j = W; j >= w[i]; j--)  // ← 逆順が重要！
 *       dp[j] = max(dp[j], dp[j-w[i]] + v[i]);
 *
 * 【完全ナップサックとの違い】
 *   - 0/1ナップサック: 各品物は1回だけ使える → j を逆順にループ
 *   - 完全ナップサック: 各品物を何回でも使える → j を順方向にループ
 *
 * 【競プロTips】
 *   - n と W が小さい（数千以下）なら 2次元DPで十分
 *   - W が巨大（10^9等）だが品物数が少ない場合は「価値で DP」に切り替える
 *     → dp[i][v] = 価値 v を達成するための最小重さ
 *   - 部分和問題（Subset Sum）はナップサックの特殊ケース（重さ=価値）
 *   - 品物のグループ化、個数制限付きなど多くのバリエーションがある
 *
 * 【学べること】0/1ナップサック、2次元DP、1次元最適化
 */
