package problems_expert;

import java.util.Arrays;

/**
 * ===== エキスパート08: Floyd-Warshall (全点対最短経路) =====
 * 出典: Codeforces / グラフ理論定番
 *
 * 【問題】重み付きグラフの全頂点ペア間の最短距離を求めよ。
 *
 * 【Floyd-Warshall アルゴリズム O(V^3)】
 *   dp[i][j] = i から j への最短距離
 *   全ての中継点 k を試す:
 *   dp[i][j] = min(dp[i][j], dp[i][k] + dp[k][j])
 *
 *   つまり「i→j に直接行くより、i→k→j と経由した方が短いなら更新」
 *
 * 【なぜ k のループが最外側か（超重要！）】
 *   dp[k][i][j] = 「中継点として頂点 {0,1,...,k} だけを使った場合の i→j の最短距離」
 *   と考える。k を最外側にすることで、k-1 までの中継点を使った最短距離が確定した後に、
 *   k を新たな中継点として追加する形で更新できる。
 *   k のループを内側にすると、未確定の値を参照してしまい正しくない。
 *
 * 【具体例】
 *   4頂点のグラフ:
 *        0 →(3)→ 1
 *        ↑       ↓
 *       (2)     (2)
 *        |       ↓
 *        3 ←(1)← 2
 *        ↓       ↑
 *       (7)→ 0  (5)
 *
 *   初期距離行列:
 *        to: 0     1     2     3
 *   from 0: [0,    3,    INF,  7  ]
 *   from 1: [8,    0,    2,    INF]
 *   from 2: [5,    INF,  0,    1  ]
 *   from 3: [2,    INF,  INF,  0  ]
 *
 *   k=0 の後（0を経由する経路を考慮）:
 *     dist[1][3] = min(INF, dist[1][0]+dist[0][3]) = min(INF, 8+7) = 15
 *     dist[2][1] = min(INF, dist[2][0]+dist[0][1]) = min(INF, 5+3) = 8
 *     dist[3][1] = min(INF, dist[3][0]+dist[0][1]) = min(INF, 2+3) = 5
 *     ...
 *
 *   k=1 の後（0,1を経由する経路を考慮）:
 *     dist[0][2] = min(INF, dist[0][1]+dist[1][2]) = min(INF, 3+2) = 5
 *     ...
 *
 *   k=2 の後（0,1,2を経由する経路を考慮）:
 *     dist[0][3] = min(7, dist[0][2]+dist[2][3]) = min(7, 5+1) = 6
 *     ...
 *
 *   最終結果:
 *        to: 0  1  2  3
 *   from 0: [0, 3, 5, 6]
 *   from 1: [5, 0, 2, 3]
 *   from 2: [3, 6, 0, 1]
 *   from 3: [2, 5, 7, 0]
 *
 * 【計算量】
 *   時間計算量: O(V^3)
 *     - 3重ループ: k × i × j = V × V × V
 *     - 各反復の処理は O(1)（比較と代入のみ）
 *   空間計算量: O(V^2)
 *     - 距離行列のサイズ（入力をそのまま書き換えるので追加メモリ不要）
 *
 * 【Dijkstra との使い分け】
 *   - 全頂点ペアの最短距離が必要で V <= 500 程度 → Floyd-Warshall が簡潔
 *   - V が大きいが E が少ない場合 → Dijkstra を V 回回す方が速い
 *   - 負の辺がある場合 → Floyd-Warshall は OK、Dijkstra は使えない
 *   - 負の閉路検出: Floyd-Warshall 後に dist[i][i] < 0 の頂点があれば負の閉路が存在
 *
 * 【競プロTips】
 *   - 実装が非常に簡単（3重ループだけ）なので、V が小さければ迷わずこれ
 *   - INF の値に注意: INF + INF がオーバーフローしないように、
 *     INF = Integer.MAX_VALUE / 2 や 10^9 程度にする
 *   - 「頂点 k を通る最短経路はあるか？」→ dist[i][k] + dist[k][j] == dist[i][j] で判定
 *   - 「ある頂点を必ず通る最短経路」→ dist[s][v] + dist[v][t] == dist[s][t] で判定
 *   - 辺の追加や頂点の追加に対して部分的に更新することも可能
 *
 * 【学べること】全点対最短経路、3重ループDP
 */
