/**
 * ===== エキスパート02: トポロジカルソート (Course Schedule) =====
 * 出典: LC #207, #210
 *
 * 【問題】
 *   N個のコースがあり、前提条件（prerequisite）がある。
 *   全コースを修了できるか？できるなら順序を出力せよ。
 *
 * 【例】
 *   numCourses=4, prerequisites=[[1,0],[2,0],[3,1],[3,2]]
 *   → [0, 1, 2, 3] または [0, 2, 1, 3]（DAG上の有効な順序）
 *
 *   グラフの形:
 *      0 → 1 → 3
 *      0 → 2 → 3
 *   0を先に取らないと1,2は取れない。1,2を先に取らないと3は取れない。
 *
 * 【トポロジカルソートとは？】
 *   有向非巡回グラフ（DAG: Directed Acyclic Graph）の頂点を、
 *   全ての辺 u→v に対して u が v より前に来るように一列に並べる操作。
 *   閉路（サイクル）があるとトポロジカルソートは存在しない。
 *
 * 【アルゴリズム: Kahn's Algorithm（BFS ベース）】
 *   1. 各頂点の入次数（in-degree: その頂点に入ってくる辺の本数）を計算
 *   2. 入次数が0の頂点をキューに入れる（前提条件がないので即座に処理可能）
 *   3. キューから取り出した頂点を結果に追加し、その頂点から出る辺を削除
 *      → 隣接頂点の入次数を1減らし、0になったらキューに追加
 *   4. 全頂点を処理できたらDAG（閉路なし）、できなかったら閉路あり
 *
 * 【具体例のトレース】
 *   numCourses=4, prerequisites=[[1,0],[2,0],[3,1],[3,2]]
 *
 *   グラフ: 0→{1,2}, 1→{3}, 2→{3}
 *   入次数: [0]=0, [1]=1, [2]=1, [3]=2
 *
 *   初期キュー: [0]（入次数0は0だけ）
 *
 *   ステップ1: キューから0を取り出し、結果=[0]
 *     0→1: 入次数[1]=1→0 → キューに追加
 *     0→2: 入次数[2]=1→0 → キューに追加
 *     キュー: [1, 2]
 *
 *   ステップ2: キューから1を取り出し、結果=[0, 1]
 *     1→3: 入次数[3]=2→1 → まだキューに入れない
 *     キュー: [2]
 *
 *   ステップ3: キューから2を取り出し、結果=[0, 1, 2]
 *     2→3: 入次数[3]=1→0 → キューに追加
 *     キュー: [3]
 *
 *   ステップ4: キューから3を取り出し、結果=[0, 1, 2, 3]
 *     キュー: 空
 *
 *   idx=4 == n=4 → 全頂点処理完了！閉路なし。
 *
 * 【計算量】
 *   時間計算量: O(V + E)
 *     - 全頂点を1回ずつキューに入れて取り出す: O(V)
 *     - 全辺を1回ずつ処理する: O(E)
 *   空間計算量: O(V + E)
 *     - グラフの隣接リスト: O(V + E)
 *     - 入次数配列: O(V)
 *     - キュー: 最悪 O(V)
 *
 * 【DFS版トポロジカルソートとの比較】
 *   - DFS版: 帰りがけ順の逆順がトポロジカル順序。閉路検出には「訪問中」状態が必要
 *   - BFS版（Kahn）: 閉路検出が自然にできる（全頂点処理できたか確認するだけ）
 *   - どちらも O(V+E) だが、Kahn の方が閉路検出が簡単で実装しやすい
 *
 * 【競プロTips】
 *   - トポロジカルソートは「依存関係の解決」に使える汎用的なテクニック
 *   - DAG上のDPの前処理として使うことが多い（トポ順にDPを回す）
 *   - 「辞書順最小のトポロジカル順序」を求める場合、Queue を PriorityQueue に変える
 *   - 閉路検出問題として出題されることも多い（全頂点処理できなければ閉路あり）
 *
 * 【学べること】DAG、トポロジカルソート、閉路検出
 */
