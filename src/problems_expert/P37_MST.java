package problems_expert;

import java.util.*;

/**
 * ===== エキスパート07: 最小全域木 (Minimum Spanning Tree) =====
 * 出典: Codeforces / AtCoder グラフ理論
 *
 * 【問題】
 *   重み付き無向グラフが与えられる。
 *   全頂点をつなぐ木で、辺の重みの合計が最小のもの（MST）を求めよ。
 *
 * 【全域木（Spanning Tree）とは？】
 *   - N 頂点のグラフで、N-1 本の辺で全頂点が連結になる部分グラフ（木）
 *   - 木なので閉路を含まない
 *   - 最小全域木（MST）は辺の重みの合計が最小の全域木
 *
 * 【例】
 *   頂点: {0, 1, 2, 3}
 *   辺: {0-1:1, 0-2:4, 1-2:2, 1-3:6, 2-3:3}
 *
 *   MSTの辺: 0-1(1), 1-2(2), 2-3(3)  合計重み = 6
 *
 *   0 --1-- 1
 *   |       |
 *   4       2     ← MST は重み 1, 2, 3 の辺を選ぶ
 *   |       |
 *   2 --3-- 3
 *       (6は使わない)
 *
 * 【Kruskal法のアルゴリズム】
 *   1. 全辺を重みの昇順にソート
 *   2. 辺を小さい順に見ていき、閉路を作らない辺だけ追加
 *   3. 閉路判定に Union-Find を使う（同じ連結成分の2頂点を結ぶ辺は閉路を作る）
 *   4. N-1 本の辺を追加したら完了（N頂点の木は必ず N-1 本の辺を持つ）
 *
 * 【Kruskal法の具体例】
 *   ソート後の辺: [0-1:1, 1-2:2, 2-3:3, 0-2:4, 1-3:6]
 *
 *   辺 0-1(1): 0と1は別グループ → union(0,1)、重み+=1、辺数=1
 *   辺 1-2(2): 1と2は別グループ → union(1,2)、重み+=2、辺数=2
 *   辺 2-3(3): 2と3は別グループ → union(2,3)、重み+=3、辺数=3 ← N-1本に到達！
 *   （辺 0-2(4): 0と2は同じグループ → スキップ）
 *   （辺 1-3(6): 未処理だが既にN-1本）
 *
 *   合計重み = 1 + 2 + 3 = 6
 *
 * 【Union-Find（素集合データ構造）】
 *   - find(x): x が属するグループの代表元を返す（経路圧縮で高速化）
 *   - union(x, y): x と y のグループを統合（ランクによる結合で木の高さを抑える）
 *   - 2つの頂点が同じグループ ↔ find(x) == find(y) ↔ 辺を追加すると閉路ができる
 *
 * 【計算量】
 *   時間計算量: O(E log E)
 *     - 辺のソート: O(E log E)
 *     - Union-Find の操作: ほぼ O(1)（逆アッカーマン関数 α(N)）× E回 ≈ O(E)
 *     - 合計: O(E log E)（ソートがボトルネック）
 *   空間計算量: O(V + E)
 *     - Union-Find: O(V)
 *     - 辺リスト: O(E)
 *
 * 【Prim法との比較】
 *   - Kruskal: 辺中心。辺をソートして小さい順に追加。疎グラフ（E が少ない）に有利
 *   - Prim: 頂点中心。PriorityQueue で最小辺を選ぶ。密グラフ（E が多い）に有利
 *   - どちらも正しいMSTを求める（MSTの一意性がない場合、異なるMSTを返すことがある）
 *
 * 【競プロTips】
 *   - Kruskal は Union-Find とセットで覚える（Union-Find は他にも多用する）
 *   - 辺のソートに (a,b) -> a[2]-b[2] を使うと int overflow の危険がある
 *     安全には Integer.compare(a[2], b[2]) を使う
 *   - 「全頂点を連結にする最小コスト」= MST問題
 *   - MST の辺の最大重みを最小化する問題も Kruskal で解ける
 *   - 最大全域木は辺の重みを降順にソートするだけ
 *
 * 【学べること】MST、Kruskal(Union-Find活用)、Prim(PQ活用)
 */
