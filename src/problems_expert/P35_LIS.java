package problems_expert;

import java.util.*;

/**
 * ===== エキスパート05: 最長増加部分列 (LIS: Longest Increasing Subsequence) =====
 * 出典: LC #300
 *
 * 【問題】
 *   整数の配列が与えられる。最長の狭義増加部分列の長さを求めよ。
 *   部分列は連続しなくてよいが、元の配列での順序は保つ。
 *
 * 【例】
 *   nums = {10, 9, 2, 5, 3, 7, 101, 18}
 *   → 4 (部分列: [2, 3, 7, 101] や [2, 3, 7, 18] など)
 *
 *   他の例:
 *   nums = {0, 1, 0, 3, 2, 3}
 *   → 4 (部分列: [0, 1, 2, 3])
 *
 * 【アプローチ1: O(n^2) DP】
 *   dp[i] = nums[i] で終わる最長増加部分列の長さ
 *
 *   遷移: dp[i] = max(dp[j] + 1) for all j < i where nums[j] < nums[i]
 *   意味: i より前にある、nums[i] より小さい要素で終わるLISの長さ + 1
 *
 *   具体例: nums = {10, 9, 2, 5, 3, 7, 101, 18}
 *     dp[0] = 1  (10 だけ)
 *     dp[1] = 1  (9 だけ。9 < 10 なので 10 の後に置けない)
 *     dp[2] = 1  (2 だけ)
 *     dp[3] = 2  (2, 5)  ← nums[2]=2 < nums[3]=5 なので dp[2]+1=2
 *     dp[4] = 2  (2, 3)  ← nums[2]=2 < nums[4]=3 なので dp[2]+1=2
 *     dp[5] = 3  (2, 5, 7) or (2, 3, 7)  ← max(dp[3], dp[4]) + 1 = 3
 *     dp[6] = 4  (2, 5, 7, 101) or (2, 3, 7, 101) ← dp[5]+1=4
 *     dp[7] = 4  (2, 5, 7, 18)  or (2, 3, 7, 18)  ← dp[5]+1=4
 *     答え: max(dp) = 4
 *
 * 【アプローチ2: O(n log n) 二分探索】
 *   tails リスト: tails[i] = 長さ i+1 の増加部分列の末尾要素の最小値
 *
 *   このリストは常にソート済みに保たれる。各 num について:
 *   - num が tails の全要素より大きい → tails に追加（LISが1延びた）
 *   - そうでない → num 以上の最小要素を二分探索で見つけて置き換え
 *
 *   具体例: nums = {10, 9, 2, 5, 3, 7, 101, 18}
 *     num=10:  tails=[]      → 追加     → tails=[10]
 *     num=9:   9<10          → 置換(0)  → tails=[9]      (10を9に改善)
 *     num=2:   2<9           → 置換(0)  → tails=[2]      (9を2に改善)
 *     num=5:   5>2           → 追加     → tails=[2, 5]
 *     num=3:   3>2, 3<5      → 置換(1)  → tails=[2, 3]   (5を3に改善)
 *     num=7:   7>3           → 追加     → tails=[2, 3, 7]
 *     num=101: 101>7         → 追加     → tails=[2, 3, 7, 101]
 *     num=18:  18>7, 18<101  → 置換(3)  → tails=[2, 3, 7, 18] (101を18に改善)
 *     答え: tails.size() = 4
 *
 *   注意: tails の中身は実際のLISとは限らない！長さだけが正しい。
 *
 * 【計算量】
 *   O(n^2) DP:
 *     時間: O(n^2) ... 2重ループ
 *     空間: O(n)   ... dp配列
 *   O(n log n) 二分探索:
 *     時間: O(n log n) ... 各要素で二分探索 O(log n) × n要素
 *     空間: O(n)       ... tails配列
 *
 * 【なぜ二分探索が正しいか】
 *   tails[i] は「長さ i+1 のLISの末尾として最良の値（最小値）」を保持している。
 *   末尾が小さいほど、後続の要素を追加しやすい（=LISを延ばしやすい）ため、
 *   常に最小の末尾を維持することが最適。
 *   tails は単調増加であることが証明できるので、二分探索が適用可能。
 *
 * 【競プロTips】
 *   - 制約が n <= 10^5 以上なら O(n log n) 解法が必要
 *   - 広義単調増加（<=）にしたい場合、二分探索を upper_bound にする
 *   - LIS の「列自体」を復元したい場合は、各要素の「前の要素」を記録して逆追跡する
 *   - セグメント木やBITを使っても O(n log n) で解ける（座標圧縮 + 区間max）
 *   - LIS問題は「二次元的な順序」の問題に帰着されることが多い
 *
 * 【学べること】DPと二分探索の組み合わせ
 */
