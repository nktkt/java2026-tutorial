package lesson30_memory;

/**
 * ===== レッスン30: Javaメモリモデル =====
 *
 * 【目標】Javaのメモリ管理の仕組みを理解する
 *
 * 【スタック(Stack)とヒープ(Heap)】
 *   スタック: メソッド呼び出しごとに作られる。ローカル変数、プリミティブ値が置かれる。
 *            メソッド終了で自動的に解放される。スレッドごとに独立。
 *            高速だがサイズが小さい（通常512KB〜1MB程度）。
 *
 *   ヒープ:  new で作られたオブジェクトが置かれる。全スレッドで共有。
 *            ガベージコレクション(GC)が不要なオブジェクトを回収する。
 *            サイズが大きい（設定により数GBまで）。
 *
 * 【メモリの図解】
 *   int x = 10;
 *     スタック: [ x = 10 ]
 *
 *   String s = new String("hi");
 *     スタック: [ s = 0xABC ]  ←参照（アドレス）
 *     ヒープ:  [ 0xABC: "hi" ] ←実際のオブジェクト
 *
 *   String s2 = s;
 *     スタック: [ s2 = 0xABC ] ← s と同じアドレスを指す
 *     ヒープ:  [ 0xABC: "hi" ] ← オブジェクトは1つだけ（コピーされない）
 *
 * 【== vs equals() の真実】
 *   == :      スタック上の値を比較。プリミティブなら値、参照なら「同じオブジェクトか」
 *   equals(): ヒープ上のオブジェクトの中身を比較
 *
 * 【Stringプール】
 *   リテラル文字列("hello")はStringプールに保存され、同じ文字列は再利用される。
 *   new String("hello") はプールを使わず新しいオブジェクトを作る。
 *
 * 【ガベージコレクション(GC)】
 *   どこからも参照されなくなったヒープ上のオブジェクトを自動で回収する。
 *   手動でメモリ解放する必要がない（C/C++との違い）。
 *   System.gc() で「GCしてください」とリクエストできるが、実行は保証されない。
 *
 * 【よくあるミス】
 *   1. String を == で比較してしまう（equals() を使うべき）
 *   2. 参照の共有を理解せず、配列やオブジェクトを「コピーした」と思い込む
 *   3. null チェックを忘れて NullPointerException が発生する
 */
public class MemoryLesson {

    public static void main(String[] args) {

        // ---------- == vs equals() の違い ----------

        // "hello" はStringリテラル（ソースコードに直接書かれた文字列）
        // Stringリテラルは「Stringプール」と呼ばれるヒープ上の特別な領域に保存される
        // 同じ内容のリテラルは1つのオブジェクトを共有するため、メモリを節約できる
        // 変数 a にはStringプール内の "hello" オブジェクトへの参照（アドレス）が格納される
        // String a = "hello";            // Stringプールから取得

        // b にも "hello" を代入する
        // Stringプールにすでに "hello" が存在するので、新しいオブジェクトは作られず、
        // a と同じオブジェクトへの参照が b に格納される
        // つまり、a と b はメモリ上の全く同じオブジェクトを指している
        // 【内部動作】JVM はリテラルを見つけるとプールを検索し、存在すれば再利用する
        // String b = "hello";            // 同じプールの参照を再利用

        // new String("hello") は、Stringプールとは別に新しいStringオブジェクトをヒープ上に作成する
        // 内容は "hello" と同じだが、メモリ上の位置（アドレス）が異なる
        // 【注意】通常、new String() は不要であり推奨されない。リテラルで十分。
        // ここでは == と equals() の違いを示すためにあえて使用している
        // String c = new String("hello"); // 新しいオブジェクトをヒープに作成

        // System.out.println("--- == vs equals() ---");

        // a == b は参照（メモリアドレス）の比較を行う
        // a と b は同じStringプール内のオブジェクトを指しているため、同じアドレスを持つ
        // したがって結果は true
        // 【重要】== はオブジェクトの「中身」ではなく「アドレス」を比較している
        // System.out.println("a == b: " + (a == b));           // true（同じ参照）

        // a == c は参照の比較を行う
        // a はStringプール内のオブジェクト、c は new で作った別のオブジェクトを指す
        // アドレスが異なるため、中身が同じ "hello" でも結果は false
        // 【これが == の罠！】内容が同じでもオブジェクトが違えば false になる
        // System.out.println("a == c: " + (a == c));           // false（異なる参照）

        // a.equals(c) は文字列の「中身」の比較を行う
        // String クラスの equals() は、文字を1文字ずつ比較して内容が同じなら true を返す
        // a の中身 "hello" と c の中身 "hello" は同じなので、結果は true
        // 【鉄則】文字列の比較は常に equals() を使う！== は使わない！
        // 【面接頻出】「== と equals() の違いを説明せよ」は Java の定番質問
        // System.out.println("a.equals(c): " + a.equals(c));   // true（中身は同じ）

        // ---------- 参照の共有（配列の例） ----------

        // 配列リテラル {1, 2, 3} で int 配列を作成する
        // arr1 にはヒープ上の配列オブジェクトへの参照が格納される
        // スタック: [arr1 = 0xDEF]  →  ヒープ: [0xDEF: {1, 2, 3}]
        // int[] arr1 = {1, 2, 3};

        // arr2 = arr1 は「配列のコピー」ではなく「参照のコピー」
        // arr2 に arr1 と同じアドレスが代入されるだけ
        // つまり arr1 と arr2 は同じ配列オブジェクトを指している
        // スタック: [arr1 = 0xDEF, arr2 = 0xDEF]  →  ヒープ: [0xDEF: {1, 2, 3}]
        // 【重要】配列を本当にコピーしたい場合は Arrays.copyOf() や clone() を使う
        // int[] arr2 = arr1;        // arr2 は arr1 と同じ配列を参照

        // arr2[0] = 99 で、arr2 が指す配列の最初の要素を 99 に変更する
        // arr1 と arr2 は同じ配列を指しているため、arr1 から見ても変更が反映される
        // ヒープ: [0xDEF: {99, 2, 3}]  ← arr1 から見ても arr2 から見ても同じ
        // 【注意】これは多くの初心者が引っかかるポイント。配列やオブジェクトは参照渡しに近い動作をする
        // int[] arr2 = arr1;        // arr2 は arr1 と同じ配列を参照
        // arr2[0] = 99;             // arr2 を変更 → arr1 も変わる！

        // System.out.println("\n--- 参照の共有 ---");

        // arr1[0] を表示すると 99 になっている（arr2 の変更が反映されている）
        // これが「参照の共有」の効果。配列はコピーされていないことの証明
        // 【実務Tips】意図せぬ変更を防ぐには、防御的コピー（defensive copy）を行う
        //   例: int[] arr2 = Arrays.copyOf(arr1, arr1.length);
        // System.out.println("arr1[0] = " + arr1[0]);  // 99（！）

        // ---------- null の扱い ----------

        // null は「何も参照していない」ことを表す特殊な値
        // Stringオブジェクトが代入されていないため、s はどのオブジェクトも指していない
        // スタック: [s = null]  →  ヒープ: （何も指していない）
        // 【null が発生する場面】
        //   - 変数を初期化していない場合（フィールドのデフォルト値は null）
        //   - メソッドが「見つからない」場合に null を返す（Map.get() など）
        //   - オブジェクトが不要になったことを明示的に示す場合
        // String s = null;            // どのオブジェクトも参照していない

        // s.length() を呼ぶと NullPointerException（NPE）が発生する
        // null に対してメソッドを呼ぶことはできないため
        // NPE は Java で最も頻繁に遭遇する実行時例外の1つ
        // 【統計】Java のバグの約70%は NPE に関連すると言われている
        // // System.out.println(s.length()); // NullPointerException!

        // null チェック: メソッドを呼ぶ前に null でないことを確認する
        // これが NPE を防ぐ最も基本的な方法
        // 【実務Tips】Java 8以降は Optional<T> を使って null を安全に扱うこともできる
        //   例: Optional.ofNullable(s).ifPresent(str -> System.out.println(str.length()));
        // 【Java 14以降】NPE のメッセージが改善され、どの変数が null かが表示されるようになった
        // // 安全なチェック
        // if (s != null) {
        //     s が null でない場合のみ length() を呼ぶ → NPE を防げる
        //     System.out.println(s.length());
        // }

        // ---------- GC（ガベージコレクション）の確認 ----------

        // System.out.println("\n--- GC ---");

        // 10万回のループで大量の String オブジェクトを作成する
        // 各イテレーションで new String("temp" + i) により新しい String がヒープ上に作られる
        // 【メモリの動き】
        //   i=0: "temp0" がヒープに作られ、temp が参照する
        //   i=1: "temp1" がヒープに作られ、temp が参照する → "temp0" は誰も参照しなくなりGC対象
        //   i=2: "temp2" がヒープに作られ、temp が参照する → "temp1" はGC対象
        //   ...以降繰り返し
        // 【重要】GC があるからこそ、10万個もオブジェクトを作ってもメモリが枯渇しない
        // C/C++ なら手動で free()/delete が必要だが、Java は自動で回収してくれる
        // for (int i = 0; i < 100000; i++) {
        //     new String("temp" + i) は毎回新しいStringオブジェクトをヒープに作る
        //     "temp" + i は文字列連結で、内部的には StringBuilder が使われることが多い
        //     String temp = new String("temp" + i);
        //     // temp はループ内で参照されなくなるとGC対象になる
        //     // 次のイテレーションでtempに新しい参照が代入されると、古いオブジェクトは到達不能になる
        // }

        // GC は JVM がバックグラウンドで自動的に実行するため、プログラマは意識する必要がない
        // ただし、GC実行中は「Stop the World」（一時停止）が発生する場合がある
        // 大量のオブジェクト生成・破棄はGC負荷を高めるため、パフォーマンスに影響する
        // 【実務Tips】不要なオブジェクト生成を減らすことがパフォーマンス改善の基本
        // System.out.println("GCが不要オブジェクトを自動回収しています");

        // ===== 練習問題 =====
        // 1. == と equals() の違いを Integer で確認してみよう
        //    Integer x = 127; Integer y = 127; → x == y は true
        //    Integer x = 128; Integer y = 128; → x == y は false（！）
        //    【理由】Java は -128〜127 の Integer をキャッシュ（プール）している
        //    この範囲内ではオートボクシングで同じオブジェクトが再利用される
        //    128 以上はキャッシュの範囲外なので、別のオブジェクトが作られる
        //    → Integer の比較も equals() を使うべき！
        //
        // 2. 配列の防御的コピーを試してみよう
        //    int[] original = {1, 2, 3};
        //    int[] copy = Arrays.copyOf(original, original.length);
        //    copy[0] = 99;
        //    → original[0] は 1 のまま（独立したコピーなので影響なし）
    }
}
